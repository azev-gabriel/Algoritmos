# Efetuar a soma de três números digitados pelo usuário e imprimir o resultado.

# Criando as variáveis:
num1 = float(input('Digite o primeiro valor: ')) # Aqui, o usuário digita um valor que é adicionado na variável.
num2 = float(input('Digite o segundo valor: '))
num3 = float(input('Digite o terceiro valor: '))

soma = num1 + num2 + num3

print(f'A soma dos valores digitados é: {soma}') # Forma mais elegante de printar...